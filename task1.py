'''
Визначити кількість трицифрових чисел, сума цифр яких дорівнює деякому
цілому значенню n, яке вводить користувач.

Автор: Савченко Валентина 31І група
'''

n = int(input("Введіть ціле число: "))

count = 0

for i in range(100, 1000):
    if((i // 100) + ((i // 10) % 10) + (i % 10)) == n:
        count += 1

print(f"Кількість трьохзначних чисел сума яких = {n} становить {count}")

